class Cake{
    private flower:number; //כמות קמח
    private egg:number; //ביצים
    private milk:number; //חלב
    private chocolate:number; //שוקולד

    constructor(flower:number, egg:number, milk:number, chocolate:number){
        this.flower=flower;
        this.egg=egg;
        this.milk=milk;
        this.chocolate=chocolate;
    }
}

export default Cake;